package com.sam.ravi.dto;

public class Fields {

	private String type;
	private String label;
	private String id;

	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
